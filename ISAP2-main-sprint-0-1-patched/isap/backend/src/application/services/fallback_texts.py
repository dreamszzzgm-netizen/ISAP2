def get_fallback_text(section_title: str) -> str:
    """Получить fallback-текст для раздела."""
    for key, text in FALLBACK_TEXTS.items():
        if key in section_title:
            return text.strip()
    return f"[Fallback-текст для раздела '{section_title}' не определён. Требуется ручное заполнение.]"


def get_fallback_sections() -> list[str]:
    """Получить список разделов с fallback-текстами."""
    return list(FALLBACK_TEXTS.keys())
