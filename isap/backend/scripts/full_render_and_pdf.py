def render_pdf(docx_path: str, pdf_path: str) -> bool:
    """Convert DOCX to PDF via LibreOffice."""
    soffice = r"C:\Program Files\LibreOffice\program\soffice.exe"
    with tempfile.TemporaryDirectory() as tmpdir:
        cmd = [soffice, "--headless", "--convert-to", "pdf", "--outdir", tmpdir, docx_path]
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=120)
        if result.returncode != 0:
            print(f"  LibreOffice error: {result.stderr}")
            return False
        # Find the generated PDF
        pdf_name = os.path.splitext(os.path.basename(docx_path))[0] + ".pdf"
        src = os.path.join(tmpdir, pdf_name)
        if os.path.exists(src):
            shutil.copy2(src, pdf_path)
            return True
        print(f"  PDF not found at {src}")
        return False


def visual_qa(pdf_path: str):
    """Visual QA — extract page info from PDF."""
    try:
        import fitz  # PyMuPDF
        doc = fitz.open(pdf_path)
        print(f"\n=== Visual QA: {pdf_path} ===")
        print(f"Pages: {len(doc)}")
        
        for page_num in range(min(len(doc), 10)):
            page = doc[page_num]
            rect = page.rect
            orient = "landscape" if rect.width > rect.height else "portrait"
            print(f"  Page {page_num + 1}: {orient} ({rect.width:.0f}x{rect.height:.0f} pts)")
            
            # Extract text sample
            text = page.get_text()[:200].replace('\n', ' | ')
            print(f"    Text sample: {text[:120]}...")
        
        if len(doc) > 10:
            print(f"  ... and {len(doc) - 10} more pages")
        
        doc.close()
    except ImportError:
        print("\nPyMuPDF not installed, skipping visual QA")
        print("Install with: pip install PyMuPDF")


def main():
    r = PmlaOoxmlFlatRenderer()

    # The flat renderer requires EVERY template placeholder to be served
    # (it surfaces unserved placeholders instead of silently dropping them,
    # unlike docxtpl). Auto-fill any template placeholder that the mock
    # context does not yet provide so the pilot renders fully.
    tpl_ph = r._extract_flat_placeholders()
    missing = sorted(tpl_ph - set(FULL_CTX.keys()))
    if missing:
        print(f"Auto-filling {len(missing)} template placeholders absent from mock:")
        for k in missing:
            FULL_CTX[k] = f"[{k}]"
        print("  " + ", ".join(missing))

    # Also make sure the empty-render context covers every template key
    # (the flat renderer rejects unserved placeholders).
    for k in tpl_ph:
        EMPTY_CTX.setdefault(k, "")

    # 1. Full render
    print("=" * 60)
    print("STEP 1: Full DOCX render")
    print("=" * 60)
    full_docx = os.path.join(FILES_DIR, "pmla_v2_rendered_test.docx")
    r.render_to_file(FULL_CTX, full_docx)
    print(f"  Saved: {full_docx} ({os.path.getsize(full_docx):,} bytes)")

    # 2. Empty render
    print("\n" + "=" * 60)
    print("STEP 2: Empty DOCX render")
    print("=" * 60)
    empty_docx = os.path.join(FILES_DIR, "pmla_v2_rendered_empty.docx")
    r.render_to_file(EMPTY_CTX, empty_docx)
    print(f"  Saved: {empty_docx} ({os.path.getsize(empty_docx):,} bytes)")

    # 3. PDF export
    print("\n" + "=" * 60)
    print("STEP 3: PDF export via LibreOffice")
    print("=" * 60)
    full_pdf = os.path.join(FILES_DIR, "pmla_v2_rendered_test.pdf")
    ok = render_pdf(full_docx, full_pdf)
    if ok:
        print(f"  Saved: {full_pdf} ({os.path.getsize(full_pdf):,} bytes)")
    else:
        print("  PDF export failed")

    empty_pdf = os.path.join(FILES_DIR, "pmla_v2_rendered_empty.pdf")
    ok2 = render_pdf(empty_docx, empty_pdf)
    if ok2:
        print(f"  Saved: {empty_pdf} ({os.path.getsize(empty_pdf):,} bytes)")

    # 4. Visual QA
    print("\n" + "=" * 60)
    print("STEP 4: Visual QA")
    print("=" * 60)
    if os.path.exists(full_pdf):
        visual_qa(full_pdf)
    if os.path.exists(empty_pdf):
        visual_qa(empty_pdf)

    print("\n" + "=" * 60)
    print("DONE")
    print("=" * 60)


if __name__ == "__main__":
    main()
